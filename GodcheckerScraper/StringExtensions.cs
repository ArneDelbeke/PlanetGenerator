            for (var i = 0; i < nonAsciiCharacters.Length; i++)
            {
                value = value.Replace(nonAsciiCharacters[i], asciiCharacters[i]);
            }

            return Regex.Replace(value, @"[^0-9a-zA-Z_]+", string.Empty);
        }

        public static string Normalize(this string value, bool replaceWhitespace = false, bool titleCase = false, bool normalize = false)
        {
            if (titleCase)
                value = value.ToTitleCase();

            if (normalize) 
                value = value.NormalizeForJson();

            if (replaceWhitespace) 
                value = Regex.Replace(value.Trim(), @"[\s]", "_");

            if (string.IsNullOrEmpty(value) || char.IsLower(value[0])) 
                return value;

            return char.ToLower(value[0]) + value.Substring(1);
        }
    }
